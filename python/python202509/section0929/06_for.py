

"""
    for 변수 in range(시작값, 종료값-1, 증감) : 
        반복할 명령어   
"""

for a in range(5):
    print(a)
    print("Python")

for b in range(1,5):
    print(b)
    print("ITWILL")

for c in range(1,5,2):
    print(c)
    print("KOREA")


